CREATE PROCEDURE [dbo].[USP_SO_OUT_CMPY_IF_SO_STAT_UPDT_LISTBYALL] (
@I_SO_NO_LIST	VARCHAR(MAX)   -- 주문번호
,@I_SO_STAT_CD	VARCHAR(100)   -- 변경할 주문상태
,@I_DEL_RESN  VARCHAR(100)     -- 삭제사유
)
/**********************************
1. 목적&기능 : KPP(외부사) 주문상태 변경을 위한 REQUEST 값 가져오기
2. 변경 내용 및 사유 :
