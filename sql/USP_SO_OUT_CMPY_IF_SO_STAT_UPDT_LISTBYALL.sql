SET NOCOUNT ON
SET LOCK_TIMEOUT 60000
SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
BEGIN
------------------------------------------------------------------
SELECT	DISTINCT
    SM.OUT_DLV_ORD_ID							AS DLV_ORD_ID 			--배송주문번호(WINUS)
                 ,SM.OUT_DLV_ORD_SEQ						AS DLV_ORD_ID_SEQ		--배송주문SEQ	1  --20220517 정연호 수정 SLV 를 DLV로 수정함
                 ,'N'										AS IMG_SND_YN			--이미지여부	 Y/N
                 ,SM.DLVY_CNFM_DT							AS DLV_REQ_DT			--배송예정일	2022
                 ,SM.DLVY_CNFM_DT							AS DLV_SET_DT								--배송완료일	2022
                 ,ISNULL(ISNULL(UM2.USER_NM,UM.USER_NM),'')	AS SET_DRIVER_ID							--지정배송기사
                 ,ISNULL(ISNULL(UM2.HP,UM.HP),'')			AS SET_DRIVER_TL							--기사전화번호
              --배송상태	'배송
                 , DLV_ORD_STAT = (SELECT TOP 1 X.ORGN_CD FROM TBL_SO_OUT_CMPY_MAPPING_CD X WITH(NOLOCK) WHERE X.OUT_CMPY_CD ='KPP' AND X.MAPPING_TYPE_CD ='SO_STAT_CD' AND  X.CNVT_CD = @I_SO_STAT_CD )

FROM DBO.TBL_SO_M AS SM WITH(NOLOCK)
    --20220608 정연호 6월 8일 개발과 운영이 다르다는것을 발견함 --이건 개발에 있던것
    LEFT OUTER JOIN TBL_INST_PLAN_M AS PM WITH(NOLOCK) ON  SM.TBL_SO_M_ID = PM.TBL_SO_M_ID
    LEFT OUTER JOIN TBL_INST_MOBILE_M AS MM WITH(NOLOCK) ON SM.TBL_SO_M_ID = MM.TBL_SO_M_ID

    --20220608 정연호 6월 8일 개발과 운영이 다르다는것을 발견함 --이건 운영에 있던것
    --LEFT OUTER JOIN TBL_INST_PLAN_M AS PM WITH(NOLOCK) ON SM.SO_NO = PM.SO_NO AND SM.TBL_SO_M_ID = PM.TBL_SO_M_ID
    --LEFT OUTER JOIN TBL_INST_MOBILE_M AS MM WITH(NOLOCK) ON PM.SO_NO = MM.SO_NO AND SM.TBL_SO_M_ID = MM.TBL_SO_M_ID AND PM.TBL_SO_M_ID = MM.TBL_SO_M_ID

    LEFT OUTER JOIN TBL_INST_MOBILE_HAPPYCALL  AS MH WITH(NOLOCK) ON MM.INST_MOBILE_M_ID = MH.INST_MOBILE_M_ID
    LEFT OUTER JOIN TBL_INST_MOBILE_COMPLETE   AS MC WITH(NOLOCK) ON MM.INST_MOBILE_M_ID =  MC.INST_MOBILE_M_ID
    LEFT OUTER JOIN TBL_INST_MOBILE_X_COMPLETE AS XMC WITH(NOLOCK) ON MM.INST_MOBILE_M_ID = XMC.INST_MOBILE_M_ID

    LEFT OUTER JOIN TBL_USER_M AS UM WITH(NOLOCK) ON PM.TBL_USER_M_ID = UM.TBL_USER_M_ID
    LEFT OUTER JOIN TBL_USER_M AS UM2 WITH(NOLOCK) ON MM.EXEC_USER_M_ID = UM2.TBL_USER_M_ID



WHERE 1=1
  AND SM.SO_NO = @I_SO_NO_LIST
