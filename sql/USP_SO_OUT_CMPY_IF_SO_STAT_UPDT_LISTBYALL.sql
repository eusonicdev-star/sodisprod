USP_SO_OUT_CMPY_IF_SO_STAT_UPDT_LISTBYALL '2204280001,2204280002,2204280003','0000',''
USP_SO_OUT_CMPY_IF_SO_STAT_UPDT_LISTBYALL '2204280001','0000',''



SELECT * FROM TBL_SO_M WHERE SO_NO IN ('2204280001','2204280002','2204280003' )

