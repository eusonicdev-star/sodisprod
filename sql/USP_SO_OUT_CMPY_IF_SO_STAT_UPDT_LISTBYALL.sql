IMG_SND_YN				VARCHAR2	1					이미지여부	 Y/N
DLV_REQ_DT				VARCHAR2	8					배송예정일	20220503
DLV_SET_DT				VARCHAR2	8					배송완료일	20220503
SET_DRIVER_ID			VARCHAR2	20					지정배송기사	송우용SM
SET_DRIVER_TL			VARCHAR2	20					기사전화번호	010-9446-1232
DLV_ORD_STAT			VARCHAR2	20	NOT NULL		배송상태	'배송상태 코드 매칭'!A1
DLV_ORD_STAT_NM			VARCHAR2	20	NOT NULL		배송상태명	'배송상태 코드 매칭'!A1
ETC2					VARCHAR2	1000				취소사유
SERIAL_NO				VARCHAR2	50					시리얼번호
DLV_COMMENT				VARCHAR2	1000				배송메모모바일
MEMO					VARCHAR2	1000				인수증메모
LIFTING_WORK_YN			VARCHAR2	1					양중여부	 Y/N
HAPPY_CALL_YN			VARCHAR2	1					해피콜여부	 Y/N
HAPPY_CALL_MEMO			VARCHAR2	1000				해피콜메모
HAPPY_CALL_DT			VARCHAR2	20					해피콜일자	2022-05-04 09:26:40
